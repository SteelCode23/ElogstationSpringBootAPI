package com.elogstation.api.elogstationapi.util;

public class EldNameAndPowerunit {
    String powerunitNumber;
    String vin;
    String trailerNumber;
    String orgId;

    public String getPowerunitNumber() {
        return powerunitNumber;
    }

    public void setPowerunitNumber(String powerunitNumber) {
        this.powerunitNumber = powerunitNumber;
    }

    public String getVin() {
        return vin;
    }

    public void setVin(String vin) {
        this.vin = vin;
    }

    public String getTrailerNumber() {
        return trailerNumber;
    }

    public void setTrailerNumber(String trailerNumber) {
        this.trailerNumber = trailerNumber;
    }

    public String getOrgId() {
        return orgId;
    }

    public void setOrgId(String orgId) {
        this.orgId = orgId;
    }
}
